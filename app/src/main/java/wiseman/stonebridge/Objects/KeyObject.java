public class KeyObject {
    private String key;
    public KeyObject(String key)
    {
        this.key = key;
    }
    public KeyObject()
    {
        this.key = key;
    }
    public void setKey(String key)
    {
        this.key = key;
    }
    public String getKey()
    {
        return  key;
    }
}
